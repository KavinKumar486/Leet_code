from typing import List
import heapq
class Solution:
    def maximumHappinessSum(self, happiness: List[int], k: int) -> int:
        heap=[]
        for i in happiness:
            heapq.heappush(heap,-i)

        decay= 0 
        res=0
        for i in range(k):
            temp =  -heapq.heappop(heap)-decay
            
            if temp>0:
                res+=temp
            else:
                break
            decay+=1
        return res

